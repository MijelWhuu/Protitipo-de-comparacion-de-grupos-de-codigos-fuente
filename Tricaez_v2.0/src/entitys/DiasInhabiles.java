package entitys;

/**
 *
 * @author mavg
 */
public class DiasInhabiles {

    int anio;
    int mes;
    int dia;
    
    public DiasInhabiles() {
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }
    
}
